        /*O "INDEX OF":
         É ultilizado para para encontrar algum indice ou paravra alguma string. Ele vai varrendo a string,
         ate encontrar o char ou a string informada como argumento. Quando encontrada ele para de execultar,
         e retorna o indice aonde começa o argumento. 
         Caso o indice não for encontrado, por conta de não existir, será retornado "-1"
        
        Sintaxe:
        
        PARAMETRO CHAR:
        **** int indiceChar =  nomeVariavel.IndexOf(Argumento Char)          
        Ele ira encontrar o indice do caractere e armazenarem uma variavel do tipo inteiro 


        PARAMETRO STRING:
        **** int indiceString = nomeVariavel.IndexOf(Argumento String)

        Ele ira encontrar a string e retornar o indice que ela se inicia.
        */

